function VerifierInput(id, idErr) {
	var error = "";
	var ok = true;
	var regExpression;
	var aVerifier = document.getElementById(id).value;
	switch (id) {
    case 'nom':
        error = "Le nom doit contenir seulement des lettres et minimum 2 lettres.";
		regExpression = new RegExp("^[a-zA-Z]{2,20}$");
		break;
    case 'courriel':
        error = "Format incorrect pour le courriel.";
		regExpression = new RegExp("^[a-zA-Z0-9.!#$%&’*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)+$");
		break;
	case 'email':
		error = "Format incorrect pour le courriel.";
		regExpression = new RegExp("^[a-zA-Z0-9.!#$%&’*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)+$");
		break;
    case 'mPasse':
        error = "Minimum 8 caractères,doit contenir au moins une letre majuscule, une lettre minuscule, un chiffre, pas d'autres caractères.";
		regExpression = new RegExp("^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])([a-zA-Z0-9]{8,})$");
		break;
	case 'mdp':
        error = "Minimum 8 caractères,doit contenir au moins une letre majuscule, une lettre minuscule, un chiffre, pas d'autres caractères.";
		regExpression = new RegExp("^(?=.*[0-9])(?=.*[a-z])(?=.*[A-Z])([a-zA-Z0-9]{8,})$");
		break;
    case 'website':
        error = "Format incorrect pour le site: www.site.xx";
		regExpression = new RegExp("^www\.[a-zA-Z0-9]{1,30}\.[a-z]{2}$");
	}
	if(!regExpression.test(aVerifier)) { document.getElementById(idErr).innerHTML = error; ok = false;}
		else {document.getElementById(idErr).innerHTML = "";}
	return ok;
}
	
